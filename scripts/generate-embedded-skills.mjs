#!/usr/bin/env node
/**
 * v1.9.7: Embedded Skills Generator
 *
 * Reads SKILL.md files, extracts content between <!-- EMBEDDED:START --> and
 * <!-- EMBEDDED:END --> markers, minifies, concatenates, writes to
 * src/core/embedded-skills.ts.
 *
 * Runs automatically on `prepublishOnly` via package.json script.
 */

import { readFileSync, writeFileSync, existsSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");

const SKILL_FILES = [
	"skills/fmcp-intent-router/SKILL.md",
	"skills/fmcp-screen-orchestrator/SKILL.md",
	"skills/figma-canvas-ops/SKILL.md",
	"skills/fmcp-screen-recipes/SKILL.md",
	"skills/fmcp-project-rules/SKILL.md",
];

const MARKER_START = "<!-- EMBEDDED:START -->";
const MARKER_END = "<!-- EMBEDDED:END -->";

function extractEmbedded(md, skillName) {
	const startIdx = md.indexOf(MARKER_START);
	const endIdx = md.indexOf(MARKER_END);
	if (startIdx !== -1 && endIdx !== -1 && endIdx > startIdx) {
		return md.slice(startIdx + MARKER_START.length, endIdx).trim();
	}
	const essMatch = md.match(/##\s+Essentials\s*\n([\s\S]+?)(?=\n##\s+Advanced|$)/);
	if (essMatch) return essMatch[1].trim();
	console.warn(`[generate-embedded-skills] ${skillName}: no markers or Essentials section; using first 200 lines fallback.`);
	return md.split("\n").slice(0, 200).join("\n").trim();
}

function minifyMarkdown(md) {
	return md
		.replace(/\n{3,}/g, "\n\n")
		.split("\n")
		.map((l) => l.replace(/[ \t]+$/, ""))
		.join("\n")
		.replace(/<!--[^>]*-->/g, "")
		.trim();
}

function estimateTokens(s) {
	return Math.ceil(s.length / 4);
}

function escapeTemplateLiteral(s) {
	return s.replace(/\\/g, "\\\\").replace(/`/g, "\\`").replace(/\$\{/g, "\\${");
}

function generate() {
	const sections = [];

	for (const relPath of SKILL_FILES) {
		const fullPath = join(ROOT, relPath);
		if (!existsSync(fullPath)) {
			console.warn(`[generate-embedded-skills] Skip missing: ${relPath}`);
			continue;
		}
		const md = readFileSync(fullPath, "utf8");
		const extracted = extractEmbedded(md, relPath);
		const minified = minifyMarkdown(extracted);
		const tokens = estimateTokens(minified);
		const skillName = relPath.split("/")[1];
		sections.push(`<!-- ${skillName} (${tokens} tokens) -->\n${minified}`);
		console.log(`[generate-embedded-skills] ${skillName}: ${tokens} tokens`);
	}

	const combined = sections.join("\n\n---\n\n");
	const combinedTokens = estimateTokens(combined);
	console.log(`[generate-embedded-skills] Total: ${combinedTokens} tokens (~${Math.round(combinedTokens / 1000)}K)`);

	const generatedAt = new Date().toISOString();
	const output = `/**
 * v1.9.7: Auto-generated embedded skill summary for Claude Desktop zero-click enforcement.
 *
 * Generated by: scripts/generate-embedded-skills.mjs
 * Source: ${SKILL_FILES.join(", ")}
 *
 * DO NOT EDIT MANUALLY. Run \`npm run generate:embedded-skills\` to regenerate.
 * This file is regenerated on prepublishOnly hook before npm publish.
 *
 * Generated: ${generatedAt}
 * Total estimated tokens: ${combinedTokens}
 */

export const EMBEDDED_SKILLS_SUMMARY = \`${escapeTemplateLiteral(combined)}\`;

export const EMBEDDED_SKILLS_TOKEN_ESTIMATE = ${combinedTokens};
export const EMBEDDED_SKILLS_VERSION = "1.9.7";
export const EMBEDDED_SKILLS_GENERATED_AT = "${generatedAt}";
`;

	const outPath = join(ROOT, "src/core/embedded-skills.ts");
	writeFileSync(outPath, output, "utf8");
	console.log(`[generate-embedded-skills] Wrote ${outPath}`);
}

generate();
